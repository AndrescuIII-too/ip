public class Proto {
    static String LINE_SEPARATOR = "-".repeat(40);

    public static void main(String[] args) {
        System.out.println(LINE_SEPARATOR + "\n" +
                "Hello! I'm Proto\n" +
                "What can I do for you?\n" +
                LINE_SEPARATOR + "\n" +
                "Bye. Hope to see you again soon!\n" +
                LINE_SEPARATOR
        );
    }
}
